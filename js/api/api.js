// ============================================================================
// LISTAR
// ============================================================================

export function listar(aba) {

    return get(

        "listar",

        aba

    );

}


// ============================================================================
// BUSCAR
// ============================================================================

export function buscar(

    aba,

    id

) {

    return get(

        "buscar",

        aba,

        id

    );

}


// ============================================================================
// SALVAR
// ============================================================================

export function salvar(

    aba,

    dados

) {

    return post(

        "salvar",

        aba,

        dados

    );

}


// ============================================================================
// EDITAR
// ============================================================================

export function editar(

    aba,

    id,

    dados

) {

    return post(

        "editar",

        aba,

        dados,

        id

    );

}


// ============================================================================
// EXCLUIR
// ============================================================================

export function excluir(

    aba,

    id

) {

    return post(

        "excluir",

        aba,

        {},

        id

    );

}

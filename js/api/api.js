import {

    CONFIG

} from "../config/config.js";



// ============================================================================
// REQUEST
// ============================================================================

async function request(

    url,

    options = {}

) {

    console.log(

        "API Request:",

        url

    );


    const resposta = await fetch(

        url,

        options

    );


    // ------------------------------------------------------------------------
    // ERRO HTTP
    // ------------------------------------------------------------------------

    if (!resposta.ok) {

        throw new Error(

            `Erro HTTP ${resposta.status}`

        );

    }


    // ------------------------------------------------------------------------
    // CONVERTER RESPOSTA
    // ------------------------------------------------------------------------

    let json;


    try {

        json = await resposta.json();

    } catch (erro) {

        throw new Error(

            "A API retornou uma resposta inválida."

        );

    }


    // ------------------------------------------------------------------------
    // DEBUG
    // ------------------------------------------------------------------------

    console.log(

        "Resposta da API:",

        json

    );


    // ------------------------------------------------------------------------
    // VALIDAR RESPOSTA
    // ------------------------------------------------------------------------

    if (

        !json ||

        typeof json !== "object"

    ) {

        throw new Error(

            "Resposta inválida da API."

        );

    }


    // ------------------------------------------------------------------------
    // ERRO DA API
    // ------------------------------------------------------------------------

    if (

        json.success === false

    ) {

        throw new Error(

            json.message ||

            "Erro desconhecido na API."

        );

    }


    // ------------------------------------------------------------------------
    // RETORNO
    // ------------------------------------------------------------------------

    return json.data ?? [];

}



// ============================================================================
// GET
// ============================================================================

async function get(

    acao,

    aba,

    id = null

) {

    const params =

        new URLSearchParams();


    params.append(

        "acao",

        acao

    );


    params.append(

        "aba",

        aba

    );


    if (

        id !== null &&

        id !== undefined

    ) {

        params.append(

            "id",

            id

        );

    }


    const url =

        `${CONFIG.API_URL}?${params.toString()}`;


    return request(

        url

    );

}



// ============================================================================
// POST
// ============================================================================

async function post(

    acao,

    aba,

    dados = {},

    id = null

) {

    return request(

        CONFIG.API_URL,

        {

            method:

                "POST",


            headers: {

                "Content-Type":

                    "text/plain;charset=utf-8"

            },


            body:

                JSON.stringify({

                    acao,

                    aba,

                    id,

                    dados

                })

        }

    );

}



// ============================================================================

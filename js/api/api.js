async function request(

    url,

    options = {}

) {

    const resposta = await fetch(

        url,

        options

    );


    // ------------------------------------------------------------------------
    // ERRO HTTP
    // ------------------------------------------------------------------------

    if (!resposta.ok) {

        throw new Error(

            `Erro HTTP ${resposta.status}`

        );

    }


    // ------------------------------------------------------------------------
    // LÊ RESPOSTA
    // ------------------------------------------------------------------------

    const json = await resposta.json();


    // ------------------------------------------------------------------------
    // DEBUG
    // ------------------------------------------------------------------------

    console.log(

        "Resposta da API:",

        json

    );


    // ------------------------------------------------------------------------
    // ERRO DA API
    // ------------------------------------------------------------------------

    if (!json.sucesso) {

        throw new Error(

            json.erro ||

            json.message ||

            json.mensagem ||

            "Erro desconhecido na API."

        );

    }


    // ------------------------------------------------------------------------
    // RETORNA DADOS
    // ------------------------------------------------------------------------

    return json.dados;

}


// ============================================================================
// GET
// ============================================================================

async function get(

    acao,

    aba,

    id = null

) {

    const params = new URLSearchParams();


    params.append(

        "acao",

        acao

    );


    params.append(

        "aba",

        aba

    );


    if (id !== null && id !== undefined) {

        params.append(

            "id",

            id

        );

    }


    return await request(

        `${CONFIG.API_URL}?${params.toString()}`

    );

}


// ============================================================================
// POST
// ============================================================================

async function post(

    acao,

    aba,

    dados = {},

    id = null

) {

    return await request(

        CONFIG.API_URL,

        {

            method: "POST",

            headers: {

                "Content-Type":

                    "text/plain;charset=utf-8"

            },

            body: JSON.stringify({

                acao,

                aba,

                id,

                dados

            })

        }

    );

}


